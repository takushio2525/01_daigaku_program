#include <stdio.h>
#include <string.h>

#include "patternlib.h"
void init_config(Config *config)
{
    config->scale = 1;
    config->horizontal_flag = 0;
    config->color_flag = 0;
    config->color = 1;
    config->disp_char = ' ';
    setFontArray(config->array);
}

void show_str_vertical_scaled(const char *str, Config config)
{

    for (const char *p = str; *p != '\0'; p++)
    {
        int char_index = conv_ASCII(*p);
        if (char_index == -1)
            continue; // 対応する文字がなければスキップ

        for (int y = 0; y < ROW_SIZE; y++)
        {
            for (int i = 0; i < config.scale; i++) // 縦方向のスケーリング
            {
                for (int x = 0; x < COL_SIZE; x++)
                {
                    for (int j = 0; j < config.scale; j++) // 横方向のスケーリング
                    {
                        if (config.color_flag)
                        {
                            print_char_colored(config.array[char_index][y][x], config.disp_char, config.color);
                        }
                        else
                        {
                            print_char(config.array[char_index][y][x], config.disp_char);
                        }
                    }
                }
                printf("\n");
            }
        }
        printf("\n"); // 文字の間に空白行
    }
}

void show_str_horizontal_scaled(const char *str, Config config)
{

    for (int y = 0; y < ROW_SIZE; y++)
    {
        for (int i = 0; i < config.scale; i++) // 縦方向のスケーリング
        {
            for (const char *p = str; *p != '\0'; p++)
            {
                int char_index = conv_ASCII(*p);
                if (char_index == -1)
                {
                    // 対応文字がない場合、空白を描画
                    for (int x = 0; x < COL_SIZE; x++)
                    {
                        for (int j = 0; j < config.scale; j++)
                        {
                            if (config.color_flag)
                            {
                                print_char_colored(0, config.disp_char, config.color);
                            }
                            else
                            {
                                print_char(0, config.disp_char);
                            }
                        }
                    }
                }
                else
                {
                    for (int x = 0; x < COL_SIZE; x++)
                    {
                        for (int j = 0; j < config.scale; j++) // 横方向のスケーリング
                        {
                            if (config.color_flag)
                            {
                                print_char_colored(config.array[char_index][y][x], config.disp_char, config.color);
                            }
                            else
                            {
                                print_char(config.array[char_index][y][x], config.disp_char);
                            }
                        }
                    }
                }
                printf(" "); // 文字間にスペース
            }
            printf("\n");
        }
    }
}

void show_str(const char *str, Config config)
{
    if (config.horizontal_flag)
    {
        show_str_horizontal_scaled(str, config);
    }
    else
    {
        show_str_vertical_scaled(str, config);
    }
}

void print_char(int flag, char _char)
{
    if (flag == 0)
    {
        printf(" ");
    }
    else
    {
        if (_char == ' ')
        {
            printf("\e[47m%c\e[0m", _char);
        }
        else
        {
            printf("%c", _char);
        }
    }
}

void print_char_colored(int flag, char _char, int color)
{
    if (flag == 0)
        printf("\e[0m ");
    else
    {
        if (_char == ' ')
        {
            printf("\e[4%dm%c\e[0m", color, _char);
        }
        else
        {
            printf("\e[3%dm%c\e[0m", color, _char);
        }
    }
}

int conv_ASCII(char _char)
{
    if (_char >= 'A' && _char <= 'Z')
    {
        return _char - 'A';
    }
    else if (_char >= 'a' && _char <= 'z')
    {
        return _char - 'a' + 26;
    }
    else if (_char >= '0' && _char <= '9')
    {
        return _char - '0' + 52;
    }
    else if (_char == '!')
    {
        return 62;
    }
    else if (_char == '?')
    {
        return 63;
    }
    return -1; // 対応する文字がない場合は-1を返す
}

